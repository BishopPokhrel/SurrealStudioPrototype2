#include "ExampleCube.h"

namespace SurrealStudio {
	
	std::shared_ptr<SurrealRenderer::Mesh> ExampleCube::CreateExampleCube() noexcept
	{
        std::vector<SurrealRenderer::Vertex> vertices = {
            // Front face
            {{-0.5f,-0.5f, 0.5f}, {0,0,1}, {0,0}},
            {{ 0.5f,-0.5f, 0.5f}, {0,0,1}, {1,0}},
            {{ 0.5f, 0.5f, 0.5f}, {0,0,1}, {1,1}},
            {{ 0.5f, 0.5f, 0.5f}, {0,0,1}, {1,1}},
            {{-0.5f, 0.5f, 0.5f}, {0,0,1}, {0,1}},
            {{-0.5f,-0.5f, 0.5f}, {0,0,1}, {0,0}},

            // Back face
            {{-0.5f,-0.5f,-0.5f}, {0,0,-1}, {0,0}},
            {{-0.5f, 0.5f,-0.5f}, {0,0,-1}, {0,1}},
            {{ 0.5f, 0.5f,-0.5f}, {0,0,-1}, {1,1}},
            {{ 0.5f, 0.5f,-0.5f}, {0,0,-1}, {1,1}},
            {{ 0.5f,-0.5f,-0.5f}, {0,0,-1}, {1,0}},
            {{-0.5f,-0.5f,-0.5f}, {0,0,-1}, {0,0}},

            // Left face
            {{-0.5f, 0.5f, 0.5f}, {-1,0,0}, {1,0}},
            {{-0.5f, 0.5f,-0.5f}, {-1,0,0}, {1,1}},
            {{-0.5f,-0.5f,-0.5f}, {-1,0,0}, {0,1}},
            {{-0.5f,-0.5f,-0.5f}, {-1,0,0}, {0,1}},
            {{-0.5f,-0.5f, 0.5f}, {-1,0,0}, {0,0}},
            {{-0.5f, 0.5f, 0.5f}, {-1,0,0}, {1,0}},

            // Right face
            {{0.5f, 0.5f, 0.5f}, {1,0,0}, {1,0}},
            {{0.5f,-0.5f,-0.5f}, {1,0,0}, {0,1}},
            {{0.5f, 0.5f,-0.5f}, {1,0,0}, {1,1}},
            {{0.5f,-0.5f,-0.5f}, {1,0,0}, {0,1}},
            {{0.5f, 0.5f, 0.5f}, {1,0,0}, {1,0}},
            {{0.5f,-0.5f, 0.5f}, {1,0,0}, {0,0}},

            // Top face
            {{-0.5f, 0.5f,-0.5f}, {0,1,0}, {0,1}},
            {{-0.5f, 0.5f, 0.5f}, {0,1,0}, {0,0}},
            {{ 0.5f, 0.5f, 0.5f}, {0,1,0}, {1,0}},
            {{ 0.5f, 0.5f, 0.5f}, {0,1,0}, {1,0}},
            {{ 0.5f, 0.5f,-0.5f}, {0,1,0}, {1,1}},
            {{-0.5f, 0.5f,-0.5f}, {0,1,0}, {0,1}},

            // Bottom face
            {{-0.5f,-0.5f,-0.5f}, {0,-1,0}, {0,1}},
            {{ 0.5f,-0.5f, 0.5f}, {0,-1,0}, {1,0}},
            {{-0.5f,-0.5f, 0.5f}, {0,-1,0}, {0,0}},
            {{ 0.5f,-0.5f, 0.5f}, {0,-1,0}, {1,0}},
            {{-0.5f,-0.5f,-0.5f}, {0,-1,0}, {0,1}},
            {{ 0.5f,-0.5f,-0.5f}, {0,-1,0}, {1,1}},
        };

        return std::make_shared<SurrealRenderer::Mesh>(
            vertices, std::vector<unsigned int>{}
        );
	}
}